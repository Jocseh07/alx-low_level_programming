/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: result of add
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two integers
 * @a: first integer
 * @b: second integer
 * Return: result of sub
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two integers
 * @a: first integer
 * @b: second integer
 * Return: result of mul
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two integers
 * @a: first integer
 * @b: second integer
 * Return: result of div
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two integers
 * @a: first integer
 * @b: second integer
 * Return: result of mod
 */

int mod(int a, int b)
{
	return (a % b);
}
